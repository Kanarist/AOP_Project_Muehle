
public class Position {
	private int x_Axis;
	private int y_Axis;
	
	public Position(int x, int y) {
		this.x_Axis = x;
		this.y_Axis = y;
	}
	
	public int getX_Axis() {
		return x_Axis;
	}
	public void setX_Axis(int x_Axis) {
		this.x_Axis = x_Axis;
	}
	
	public int getY_Axis() {
		return y_Axis;
	}
	public void setY_Axis(int y_Axis) {
		this.y_Axis = y_Axis;
	}
	
	
	
	
}
